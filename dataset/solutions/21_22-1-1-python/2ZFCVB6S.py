def springfield(n):
    if n == 0:
        return 1
    if n % 2 == 0:
        homer = springfield(n / 2)
        return (homer * homer) % 1000000007
    return (2 * springfield(n - 1))  % 1000000007

def calc(n0):
    return (springfield(n0) - 1) % 1000000007












